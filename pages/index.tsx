import { useRef, useState, FormEvent, ChangeEvent } from 'react';
import type { NextPage } from 'next';
import Head from 'next/head';
import { Grid, RestInput, Paper, Result, Tabs, Tab, TabPanel } from '@components/.';
import { PageLayout } from '@components/layouts';

type errorProps = {
  error: string;
  message: string;
};

const Home: NextPage = () => {
  const searchRef = useRef<HTMLInputElement | null>(null);
  const [result, setResult] = useState(null);
  const [selectedTab, setSelectedTab] = useState(0);
  const [error, setError] = useState<errorProps | null>(null);

  const onSearch = async (e: FormEvent) => {
    e.preventDefault();

    const call = searchRef?.current?.value;

    const response = await fetch(`${process.env.NEXT_PUBLIC_APIURL}/v1/${call}`);
    const data = await response.json();
    setError(null);
    setResult(data);
  };

  const handleTabChange = (event: ChangeEvent<{}>, newValue: number) => {
    setSelectedTab(newValue);
  };

  return (
    <>
      <Head>
        <title>Suske en Wiske API</title>
        <meta name='description' content='Generated by create next app' />
        <link rel='icon' href='/favicon.ico' />
      </Head>

      <PageLayout>
        <Grid container spacing={3}>
          <Grid item xs={12}>
            <Tabs value={selectedTab} onChange={handleTabChange}>
              <Tab label='REST API' />
              <Tab label='GraphQL' />
            </Tabs>
          </Grid>
          <Grid item xs={12}>
            <TabPanel value={selectedTab} index={0}>
              <form onSubmit={onSearch}>
                <RestInput inputRef={searchRef} />
              </form>
              <Paper>
                <Result>
                  {error && JSON.stringify(error, null, 2)}
                  {result && JSON.stringify(result, null, 2)}
                </Result>
              </Paper>
            </TabPanel>
            <TabPanel value={selectedTab} index={1}>
              Coming soon
            </TabPanel>
          </Grid>
        </Grid>
      </PageLayout>
    </>
  );
};

export default Home;
